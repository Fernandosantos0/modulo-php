    Cookies são um mecanismo para guardar dados do lado do cliente (browser).
    Podem ser usados para várias finalidades. Sessões de utilizador é a 
    finalidade mais recorrente. MAs podemos guardar outros tipos de informação.

    IMPORTANTE: Numca devemos usar cookies para guardar informação sensível,
    uma vez que a leitura dos dados guardados pode ser feita co facilidade.
    Nunca devemos esperar que o cookie exista do lado do cliente, poque o mesmo
    pode remover os cookies ou alterar os seu valores esperados.

    Podemos criar mais do que um cookie por cada site
